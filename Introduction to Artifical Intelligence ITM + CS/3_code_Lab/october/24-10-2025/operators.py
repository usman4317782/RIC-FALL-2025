'''
arithmetic operator in python 

+  : addition
-  : subtraction
*  : multiplication
/  : division
%  : modulus
// : floor division
** : exponent

'''

firstNumber = int(input ("Enter first number: "))
secondNumber = int(input ("Enter second number: "))

# // : floor division
print (f"Floor division: {firstNumber // secondNumber}")

# ** : exponent

print (f"Exponent: {firstNumber ** secondNumber}")